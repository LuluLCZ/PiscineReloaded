#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

char	ft_last_param(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int     main(int ac, char **av)
{
    if (ac > 1)
        ft_last_param(av[ac - 1]);
    ft_putchar('\n');
    return (0);
}